function Even(k) {

    if ((k % 2) == 0) {
        return true
    }

    else {
        return false
    }

}

let firstNum = 2
let secondNum = 18
let thirdNum = 5

console.log(firstNum + " = " + Even(firstNum))
console.log(secondNum + " = " + Even(secondNum))
console.log(thirdNum + " = " + Even(thirdNum))
